#include <iostream>
using namespace std;

int main() {

    // numbers array declare aur initialize ki ja rahi hai
    int numbers[5] = {10, 20, 30, 40, 50};

    // loop ke zariye array ke tamam elements display kiye ja rahe hain
    for (int i = 0; i < 5; i++) {
        cout << numbers[i] << endl;
    }

    return 0;
}
